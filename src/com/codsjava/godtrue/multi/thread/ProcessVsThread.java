public class ProcessVsThread {
/*
Process
    Process has its own main memory for execution.
    Process is considered as heavyweight component.
    One process can have multiple threads.
    Context switch time is more.

Thread
    Thread use process’s main memory for execution and share it with other threads.
    Thread is considered as lightweight component.
    One thread can’t have multiple process.
    Context switch time is less.
*/
}
